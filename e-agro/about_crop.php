                     	<h2>&nbsp Jowar</h2>
                     <p> 
		     <ul>
			<li>
			When jowar is to be cultivated, the soil should be made free from weeds. During the first ploughing, the soil should be turned, so that there is loose soil up to a depth of about 20-25 cms.			</li>
			<li>
			Planking is then followed allowing the clods to break and make the field leveled. For optimum produce from the seeds, these should be procured from genuine sources and should be free from insects			</li>
			<li>
			Jowar is a tropical crop. It grows well at a temperature between 27° and 32°C but temperature condition below 16°C is harmful for the crop. Jowar requires a moderate rainfall between 30 and 65 cm. 			</li>
			<li>
			The crop can be grown on a variety of soils ranging from heavy and light alluvium to red, grey and yellow loams and even sandy soils.		
			</li>
			<li>
			It is grown both as a dry and an irrigated crop. Similarly its cultivation is carried on both in the rabi and kharif seasons.
